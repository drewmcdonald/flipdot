/**
 * Get font name for cache keys
 */
export function getFontName(): string {
  return FONT_NAME;
}

/**
 * Get bitmap for a single character
 * Returns placeholder for unsupported characters
 */
export function renderChar(char: string): CharBitmap {
  const upperChar = char.toUpperCase();
  if (upperChar in FONT_DATA) {
    return FONT_DATA[upperChar];
  }
  // Return space for unknown characters
  return FONT_DATA[" "];
}

/**
 * Measure text width in pixels (including spacing)
 */
export function measureText(text: string): number {
  if (text.length === 0) return 0;
  return text.length * (CHAR_WIDTH + CHAR_SPACING) - CHAR_SPACING;
}

/**
 * Render text at a specific X offset (for scrolling)
 * All glyphs are pre-padded to CHAR_HEIGHT with baseline alignment.
 * This function positions the font block on the display.
 * Returns a flat bit array for the display.
 */
export function renderTextAtOffset(
  text: string,
  displayWidth: number,
  displayHeight: number,
  xOffset: number,
): number[] {
  // Calculate y-offset to align font baseline to display baseline
  const yOffset = DISPLAY_BASELINE - BASELINE_OFFSET;

  // Initialize empty display buffer
  const bits = new Array(displayWidth * displayHeight).fill(0);

  // Render each character
  let cursorX = xOffset;
  for (let i = 0; i < text.length; i++) {
    const charBitmap = renderChar(text[i]);

    // Draw character to buffer
    for (let row = 0; row < CHAR_HEIGHT; row++) {
      for (let col = 0; col < CHAR_WIDTH; col++) {
        const x = cursorX + col;
        const y = yOffset + row;

        // Bounds check
        if (x >= 0 && x < displayWidth && y >= 0 && y < displayHeight) {
          const bitIndex = y * displayWidth + x;
          bits[bitIndex] = charBitmap[row][col];
        }
      }
    }

    cursorX += CHAR_WIDTH + CHAR_SPACING;
  }

  return bits;
}

/**
 * Generate scrolling animation frames for text
 * Text scrolls from right to left across the display
 *
 * @param text Text to scroll
 * @param displayWidth Display width in pixels
 * @param displayHeight Display height in pixels
 * @param frameDelayMs Delay between frames in milliseconds
 * @returns Array of bit arrays, one per frame
 */
export function renderScrollingText(
  text: string,
  displayWidth: number,
  displayHeight: number,
  frameDelayMs: number = 100,
): Array<{ bits: number[]; duration_ms: number }> {
  const textWidth = measureText(text);
  const frames: Array<{ bits: number[]; duration_ms: number }> = [];

  // Start position: text completely off-screen to the right
  // End position: text completely off-screen to the left
  const startX = displayWidth;
  const endX = -textWidth;

  // Generate frames scrolling from right to left, one pixel at a time
  for (let x = startX; x >= endX; x--) {
    const bits = renderTextAtOffset(text, displayWidth, displayHeight, x);
    frames.push({
      bits,
      duration_ms: frameDelayMs,
    });
  }

  return frames;
}

/**
 * Render text to a flat bit array (28×14 pixels = 392 bits)
 * All glyphs are pre-padded to CHAR_HEIGHT with baseline alignment.
 * Text is centered horizontally, baseline-aligned vertically.
 */
export function renderText(
  text: string,
  displayWidth: number,
  displayHeight: number,
): number[] {
  const textWidth = measureText(text);

  // Calculate horizontal centering offset
  const xOffset = Math.floor((displayWidth - textWidth) / 2);
  // Calculate y-offset to align font baseline to display baseline
  const yOffset = DISPLAY_BASELINE - BASELINE_OFFSET;

  // Initialize empty display buffer
  const bits = new Array(displayWidth * displayHeight).fill(0);

  // Render each character
  let cursorX = xOffset;
  for (let i = 0; i < text.length; i++) {
    const charBitmap = renderChar(text[i]);

    // Draw character to buffer
    for (let row = 0; row < CHAR_HEIGHT; row++) {
      for (let col = 0; col < CHAR_WIDTH; col++) {
        const x = cursorX + col;
        const y = yOffset + row;

        // Bounds check
        if (x >= 0 && x < displayWidth && y >= 0 && y < displayHeight) {
          const bitIndex = y * displayWidth + x;
          bits[bitIndex] = charBitmap[row][col];
        }
      }
    }

    cursorX += CHAR_WIDTH + CHAR_SPACING;
  }

  return bits;
}
